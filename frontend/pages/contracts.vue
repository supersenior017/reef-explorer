<template>
  <div>
    <section>
      <b-container class="page-contracts main py-5">
        <b-row class="mb-2">
          <b-col cols="12">
            <h1>
              {{ $t('pages.contracts.title') }}
              <small v-if="totalRows !== 1" class="ml-1" style="font-size: 1rem"
                >[{{ formatNumber(totalRows) }}]</small
              >
            </h1>
          </b-col>
        </b-row>
        <div class="contracts">
          <div v-if="loading" class="text-center py-4">
            <Loading />
          </div>
          <template v-else>
            <b-alert variant="info" class="text-center" show dismissible>
              <p class="mb-0">
                <font-awesome-icon icon="file-code" />
                Are you a developer? Verify and publish your contract source
                code
                <nuxt-link to="/verifyContract" class="alert-link"
                  >here</nuxt-link
                >
              </p>
            </b-alert>
            <!-- Filter -->
            <b-row style="margin-bottom: 1rem">
              <b-col cols="12">
                <b-form-input
                  id="filterInput"
                  v-model="filter"
                  type="search"
                  :placeholder="$t('pages.contracts.search_placeholder')"
                />
              </b-col>
            </b-row>
            <div class="table-responsive">
              <b-table striped hover :fields="fields" :items="contracts">
                <template #cell(block_height)="data">
                  <p class="mb-0">
                    <nuxt-link
                      :to="`/block?blockNumber=${data.item.block_height}`"
                    >
                      #{{ formatNumber(data.item.block_height) }}
                    </nuxt-link>
                  </p>
                </template>
                <template #cell(name)="data">
                  <p class="mb-0">
                    <nuxt-link :to="`/contract/${data.item.contract_id}`">
                      {{ data.item.name }}
                    </nuxt-link>
                  </p>
                </template>
                <template #cell(contract_id)="data">
                  <p class="mb-0">
                    <eth-identicon
                      :address="data.item.contract_id"
                      :size="16"
                    />
                    <nuxt-link
                      v-b-tooltip.hover
                      :to="`/contract/${data.item.contract_id}`"
                      title="Check contract information"
                    >
                      {{ data.item.contract_id }}
                    </nuxt-link>
                  </p>
                </template>
                <template #cell(verified)="data">
                  <font-awesome-icon
                    v-if="data.item.verified"
                    icon="check"
                    class="text-success"
                  />
                  <font-awesome-icon v-else icon="times" class="text-danger" />
                </template>
              </b-table>
            </div>
            <!-- pagination -->
            <div class="row">
              <div class="col-6">
                <!-- desktop -->
                <div class="d-none d-sm-none d-md-none d-lg-block d-xl-block">
                  <b-button-group>
                    <b-button
                      v-for="(option, index) in paginationOptions"
                      :key="index"
                      variant="outline-secondary"
                      :class="{ 'selected-per-page': perPage === option }"
                      @click="setPageSize(option)"
                    >
                      {{ option }}
                    </b-button>
                  </b-button-group>
                </div>
                <!-- mobile -->
                <div class="d-block d-sm-block d-md-block d-lg-none d-xl-none">
                  <b-dropdown
                    class="m-md-2"
                    text="Page size"
                    variant="outline-secondary"
                  >
                    <b-dropdown-item
                      v-for="(option, index) in paginationOptions"
                      :key="index"
                      @click="setPageSize(10)"
                    >
                      {{ option }}
                    </b-dropdown-item>
                  </b-dropdown>
                </div>
              </div>
              <div class="col-6">
                <b-pagination
                  v-model="currentPage"
                  :total-rows="totalRows"
                  :per-page="perPage"
                  aria-controls="my-table"
                  variant="dark"
                  align="right"
                ></b-pagination>
              </div>
            </div>
          </template>
        </div>
      </b-container>
    </section>
  </div>
</template>

<script>
import { gql } from 'graphql-tag'
import commonMixin from '@/mixins/commonMixin.js'
import Loading from '@/components/Loading.vue'
import { paginationOptions } from '@/frontend.config.js'

export default {
  components: {
    Loading,
  },
  mixins: [commonMixin],
  data() {
    return {
      loading: true,
      filter: '',
      contracts: [],
      paginationOptions,
      perPage: localStorage.paginationOptions
        ? parseInt(localStorage.paginationOptions)
        : 10,
      currentPage: 1,
      totalRows: 1,
      fields: [
        {
          key: 'name',
          label: 'Name',
          sortable: true,
        },
        {
          key: 'contract_id',
          label: 'Contract address',
          sortable: true,
        },
        {
          key: 'block_height',
          label: 'Created at block',
          sortable: true,
        },
        {
          key: 'verified',
          label: 'Verified',
          sortable: true,
        },
      ],
    }
  },
  methods: {
    setPageSize(num) {
      localStorage.paginationOptions = num
      this.perPage = parseInt(num)
    },
  },
  apollo: {
    $subscribe: {
      contracts: {
        query: gql`
          subscription contract(
            $blockHeight: bigint
            $contractId: String
            $perPage: Int!
            $offset: Int!
          ) {
            contract(
              limit: $perPage
              offset: $offset
              where: {
                block_height: { _eq: $blockHeight }
                contract_id: { _eq: $contractId }
              }
              order_by: { block_height: desc }
            ) {
              contract_id
              name
              bytecode
              value
              gas_limit
              storage_limit
              signer
              block_height
              timestamp
              verified
            }
          }
        `,
        variables() {
          return {
            blockHeight: this.isBlockNumber(this.filter)
              ? parseInt(this.filter)
              : undefined,
            contractId: this.isContractId(this.filter)
              ? this.filter
              : undefined,
            perPage: this.perPage,
            offset: (this.currentPage - 1) * this.perPage,
          }
        },
        result({ data }) {
          this.contracts = data.contract
          if (this.filter) {
            this.totalRows = this.contracts.length
          }
          this.loading = false
        },
      },
      totalContracts: {
        query: gql`
          subscription total {
            total(where: { name: { _eq: "contracts" } }, limit: 1) {
              count
            }
          }
        `,
        result({ data }) {
          if (!this.filter) {
            this.totalRows = data.total[0].count
          }
        },
      },
    },
  },
}
</script>
